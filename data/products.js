export function getProduct(collection, slug) {
  const list = collection === 'luxury' ? luxuryProducts : modernProducts
  return list.find(p => p.slug === slug) || null
}

export function imgUrl(collection, slug, idx) {
  return `/images/${collection}/${slug}/pouf-${collection}-${slug}-${String(idx).padStart(2, '0')}.jpg`
}
